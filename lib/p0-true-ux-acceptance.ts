export function workflow15CoverageSummary(entries = workflow15CoverageMatrix) {
  return entries.reduce<Record<Workflow15CoverageStatus, number>>(
    (summary, entry) => {
      summary[entry.status] += 1;
      return summary;
    },
    {
      BLOCKED: 0,
      CONFLICTING: 0,
      COVERED: 0,
      MISSING: 0,
      PARTIAL: 0,
    },
  );
}

export function workflow15UnresolvedReleaseBlockers(entries = workflow15CoverageMatrix) {
  return entries.filter((entry) => {
    if (entry.status === "MISSING" || entry.status === "CONFLICTING") return true;

    return entry.blocksV096Acceptance && (entry.status === "BLOCKED" || entry.status === "PARTIAL");
  });
}
